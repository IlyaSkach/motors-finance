import React from 'react';

const TabMenu = ({ tabs, activeTab, onTabChange }) => {
  return (
    <div className="tab-menu">
      {tabs.map((tab) => (
        <button
          key={tab.id}
          className={`tab-item ${activeTab === tab.id ? 'active' : ''}`}
          onClick={() => onTabChange(tab.id)}
        >
          {tab.label}
        </button>
      ))}
    </div>
  );
};

export default TabMenu;